    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        int resp =  JOptionPane.showConfirmDialog(null,  "deseja excluir?", "Pergunta", JOptionPane.YES_NO_OPTION);
       if(resp == JOptionPane.YES_OPTION){
       EntregaNvb entregaNvb = viewBean();
        Entrega_DAO entrega_DAO = new Entrega_DAO();
        Entrega_DAO.delete(entregaNvb);
      }else{
           Util.mensagem("Excluido");
          Util.habilitar(false);
          
       }
        Util.habilitar(true);
        Util.limparCampos();       
              
    }//GEN-LAST:event_jBtnExcluirActionPerformed

